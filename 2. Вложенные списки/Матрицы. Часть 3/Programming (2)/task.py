# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –î–ª—è –≤—ã–≤–æ–¥–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Ç—Ä–∏—Ü—ã –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–∞—Ö, –æ—Ç–≤–æ–¥–∏—Ç–µ —Ä–æ–≤–Ω–æ 3 —Å–∏–º–≤–æ–ª–∞ –Ω–∞ –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç. –î–ª—è —ç—Ç–æ–≥–æ
# –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust(). –ú–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ ljust(), —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–∏–º–µ—Ç –∏ —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ üòá


n, m = input().split()
n, m = int(n), int(m)
matrix = [[int(i + j * m) for i in range(1, m + 1)] for j in range(n)]
for i in range(n):
    print(''.join(str(x).ljust(3) for x in matrix[i]))
