# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –î–ª—è –≤—ã–≤–æ–¥–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –º–∞—Ç—Ä–∏—Ü—ã –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–∞—Ö, –æ—Ç–≤–æ–¥–∏—Ç–µ —Ä–æ–≤–Ω–æ 3 —Å–∏–º–≤–æ–ª–∞ –Ω–∞ –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç. –î–ª—è —ç—Ç–æ–≥–æ
# –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å—Ç—Ä–æ–∫–æ–≤—ã–π –º–µ—Ç–æ–¥ ljust(). –ú–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ ljust(), —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–∏–º–µ—Ç –∏ —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ üòá


n, m = input().split()
n, m = int(n), int(m)
matrix = [[0 for j in range(m)] for i in range(n)]
counter = 0
step = 0

if m == 1:
    for i in range(0 + step, n - step):
        counter += 1
        matrix[i][m - 1 - step] = counter
if n == 1:
    for j in range(0 + step, m - step):
        counter += 1
        matrix[0 + step][j] = counter

while counter != n * m:
    for j in range(0 + step, m - step):
        counter += 1
        matrix[0 + step][j] = counter
    for i in range(1 + step, n - step):
        counter += 1
        matrix[i][m - 1 - step] = counter
    for j in range(m - 2 - step, -1 + step, -1):  # m - 2
        counter += 1
        matrix[n - 1 - step][j] = counter
    for i in range(n - 2 - step, 0 + step, -1):
        counter += 1
        matrix[i][0 + step] = counter
    step += 1

for i in range(n):
    print("".join(str(x).ljust(3) for x in matrix[i]))
