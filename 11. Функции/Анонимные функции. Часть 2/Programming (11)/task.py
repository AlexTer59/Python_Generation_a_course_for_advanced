# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 3. –ù–µ –∑–∞–±—É–¥—å—Ç–µ –≤—ã–∑–≤–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é evaluate(), —á—Ç–æ–±—ã –≤—ã–≤–µ—Å—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç üòÄ.


from functools import reduce


def evaluate(coefficients, x):
    return reduce(lambda acc, true_x: acc + true_x, (
        map(lambda coef, i: int(coef) * x ** (len(coefficients) - 1 - i), coefficients,
            [i for i in range(len(coefficients))])), 0)


print(evaluate((input().split()), int(input())))
